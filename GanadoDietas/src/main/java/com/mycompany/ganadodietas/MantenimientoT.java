    private void btnActuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActuActionPerformed

        CTabla tabla = new CTabla();
        // int 1 = crear, 2 = editar, 3 = eliminar, 4 = reactivar, 5 = inactivar , 6 = cancelar
        switch (flag) {
            case 1:
                tabla.Insertar(tablaSelec, txtId, txtId, textEstReg, TFlag);
                inactivar();
                TFlag.setText("0");
                inactivar();
                ActivarF();
                Limpiar();
                break;
            case 2:
                tabla.Modificar(tablaSelec, txtId, txtId, textEstReg, TFlag);
                TFlag.setText("0");
                inactivar();
                ActivarF();
                Limpiar();
                break;
            case 3:
                tabla.Eliminar(tablaSelec, txtId, txtId, textEstReg, TFlag);
                inactivar();
                break;
            case 4:
                tabla.Reactivar(tablaSelec, txtId, txtId, textEstReg, TFlag);
                inactivar();
                break;
            case 5:
                tabla.Inactivar(tablaSelec, txtId, txtId, textEstReg, TFlag);
                inactivar();
                break;
            case 6:
                inactivar();
                break;
        }
        tabla.Mostrar(tablaQ, tablaSelec);
    }//GEN-LAST:event_btnActuActionPerformed

