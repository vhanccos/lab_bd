    private void borrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_borrarActionPerformed
        int respuesta = javax.swing.JOptionPane.showConfirmDialog(null, "Â¿EstÃ¡ seguro de que desea eliminar este registro?", "Confirmar eliminaciÃ³n", javax.swing.JOptionPane.YES_NO_OPTION);
        CTabla tabla = new CTabla();

        if (respuesta == javax.swing.JOptionPane.YES_OPTION) {
            flag = 3;
            textEstReg.setText("*");
            tabla.Eliminar(tablaSelec, txtId, txtId, textEstReg, TFlag);
            TFlag.setText("0");

            inactivar();
            // AquÃ­ puedes agregar el cÃ³digo para eliminar el registro de la base de datos
            javax.swing.JOptionPane.showMessageDialog(null, "El registro ha sido marcado para eliminaciÃ³n.");
            tabla.Mostrar(tablaQ, tablaSelec);

        } else {
            // AcciÃ³n cancelada, no hacer nada
            javax.swing.JOptionPane.showMessageDialog(null, "La acciÃ³n de eliminaciÃ³n ha sido cancelada.");

        }
        ActivarF();

        Limpiar();
        inactivar();
    }//GEN-LAST:event_borrarActionPerformed

